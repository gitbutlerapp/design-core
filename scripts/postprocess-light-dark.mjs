import { spawn } from "node:child_process";
import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";
import { generateShadowVars } from "./generate-shadow-vars.mjs";

const TOKENS_CSS_PATH = resolve("tokens/tokens.css");

const isWatchMode = process.argv.includes("--watch");

function getBlock(css, selector) {
	const escaped = selector.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
	const match = css.match(
		new RegExp(`${escaped}\\s*\\{([\\s\\S]*?)\\}`, "m"),
	);
	return match?.[1] ?? "";
}

function parseDeclarations(block) {
	const declarations = [];
	const index = new Map();

	for (const line of block.split("\n")) {
		const match = line.match(
			/^\s*(--[^:]+):\s*(.+?);\s*(?:\/\*.*\*\/)?\s*$/,
		);
		if (!match) continue;

		const [, name, value] = match;
		index.set(name, declarations.length);
		declarations.push({ name, value });
	}

	return {
		declarations,
		index,
	};
}

function stringifyDeclarations(declarations) {
	return declarations
		.map(({ name, value }) => `  ${name}: ${value};`)
		.join("\n");
}

function buildLightDarkCss(sourceCss) {
	const lightBlock = getBlock(sourceCss, ":root");
	const darkBlock = getBlock(sourceCss, ":root.dark");

	if (!lightBlock || !darkBlock) {
		if (isWatchMode) {
			return sourceCss;
		}

		throw new Error(
			"Expected both :root and :root.dark token blocks in tokens/tokens.css.",
		);
	}

	const light = parseDeclarations(lightBlock);
	const dark = parseDeclarations(darkBlock);
	const merged = light.declarations.map(({ name, value }) => {
		const darkIndex = dark.index.get(name);
		if (darkIndex === undefined) {
			return { name, value };
		}

		const darkValue = dark.declarations[darkIndex].value;
		if (value === darkValue) {
			return { name, value };
		}

		return { name, value: `light-dark(${value}, ${darkValue})` };
	});

	for (const declaration of dark.declarations) {
		if (light.index.has(declaration.name)) continue;
		merged.push(declaration);
	}

	return `/* -------------------------------------------\n *  Autogenerated by ⛋ Terrazzo. DO NOT EDIT!\n * ------------------------------------------- */\n\n:root {\n  color-scheme: light dark;\n${stringifyDeclarations(merged)}\n}\n\n:root.light {\n  color-scheme: light;\n}\n\n:root.dark {\n  color-scheme: dark;\n}\n`;
}

function postprocessTokens() {
	const sourceCss = readFileSync(TOKENS_CSS_PATH, "utf8");
	const withLightDark = buildLightDarkCss(sourceCss);
	const outputCss = generateShadowVars(withLightDark);
	if (outputCss !== sourceCss) {
		writeFileSync(TOKENS_CSS_PATH, outputCss, "utf8");
	}
}

const tzArgs = ["tz", "build"];
if (isWatchMode) {
	tzArgs.push("--watch");
}

const child = spawn("npx", tzArgs, {
	cwd: process.cwd(),
	stdio: ["inherit", "pipe", "pipe"],
});

child.stdout.setEncoding("utf8");
child.stdout.on("data", (chunk) => {
	process.stdout.write(chunk);
	if (
		chunk.includes("tokens built") ||
		(!isWatchMode && chunk.includes("Build complete"))
	) {
		postprocessTokens();
	}
});

child.stderr.setEncoding("utf8");
child.stderr.on("data", (chunk) => {
	process.stderr.write(chunk);
});

child.on("close", (code) => {
	if (code === 0 && !isWatchMode) {
		postprocessTokens();
	}

	process.exit(code ?? 0);
});
